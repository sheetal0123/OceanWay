package yaml.dumpobj;

public class PlayGroup extends Education{

	String pgName;
	int pgStudentCount;
	String pgEstYear;
	boolean isFullyGovtFunded;
	
	public String getPgName() {
		return pgName;
	}
	public void setPgName(String pgName) {
		this.pgName = pgName;
	}
	public int getPgStudentCount() {
		return pgStudentCount;
	}
	public void setPgStudentCount(int pgStudentCount) {
		this.pgStudentCount = pgStudentCount;
	}
	public String getPgEstYear() {
		return pgEstYear;
	}
	public void setPgEstYear(String pgEstYear) {
		this.pgEstYear = pgEstYear;
	}
	public boolean isFullyGovtFunded() {
		return isFullyGovtFunded;
	}
	public void setFullyGovtFunded(boolean isFullyGovtFunded) {
		this.isFullyGovtFunded = isFullyGovtFunded;
	}
	
	
}
